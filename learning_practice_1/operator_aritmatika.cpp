#include <iostream>
using namespace std;

int main() {
	
	int a, b;
	// float a, b;
	
	cout<<"Value Pertama : ";
	cin >> a;
	
	cout<<"Value Kedua : ";
	cin >> b;
	
	
	cout<<"Penjumlahan dari hasil value pertama + value kedua adalah : " << a + b <<endl; 
	cout<<"Pengurangan dari hasil value pertama - value kedua adalah : " << a - b << endl;
	cout<<"Perkalian dari hasil value pertama - value kedua adalah : " << a * b << endl;
	cout<<"Pembagian dari hasil value pertama - value kedua adalah : " << a / b << endl;
	cout<<"Modulus dari hasil value pertama - value kedua adalah : " << a % b << endl;
	
	return 0;
}
