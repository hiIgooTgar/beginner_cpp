#include <stdio.h>

int main() {
	int a,b,c;
	
	printf("Input value a : \n");
	scanf("%d", &a);
	
	printf("Input value b : \n");
	scanf("%i", &b);
	
	c = a + b;
	printf("The result of value a + value b is %i", c);
	
}

// Simbol & berfungsi untuk mengambil alamat memori dari sebuah variabel.
// Fungsi scanf() membutuhkan tempat untuk menyimpan nilai yang akan diinputkan.
